    private void savePreferences() {
        try {
            Preferences.savePreferences();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, new String[] {"Error saving preferences!", e.toString()}, "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * @param args the command line arguments
     */
    public static void main(String args[]) {
        try {
            Preferences.loadPreferences();
        } catch (IOException ioe) {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(), new String[] {"Unable to load WebScarab properties!", ioe.toString()}, "Error!", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
        }
        Framework framework = new Framework();
        
        WebScarab ws = new WebScarab(framework);
        try {
            Properties prefs = Preferences.getPreferences();
            int xpos = Integer.parseInt(prefs.getProperty("WebScarab.position.x"));
            int ypos = Integer.parseInt(prefs.getProperty("WebScarab.position.y"));
            int width = Integer.parseInt(prefs.getProperty("WebScarab.size.x"));
            int height = Integer.parseInt(prefs.getProperty("WebScarab.size.y"));
            ws.setBounds(xpos,ypos,width,height);
        } catch (NumberFormatException nfe) {
            System.out.println("Number format exception parsing preferred size and position: " + nfe);
        } catch (NullPointerException npe) {
        }
        ws.show();
        
        ws.addPlugin(new ProxyPanel(framework));
        ws.addPlugin(new SpiderPanel(framework));
        ws.addPlugin(new ManualRequestPanel(framework));
        ws.addPlugin(new SessionIDPanel(framework));
        
        boolean loaded = false;
        if (args != null && args.length == 1 && !args[0].equals("")) {
            loaded = ws.loadSession(args[0]);
        } else {
            JFileChooser jfc = new JFileChooser();
            jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            jfc.setDialogTitle("Select a directory to write the session into");
            int returnVal = jfc.showOpenDialog(ws);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = jfc.getSelectedFile();
                String dir = file.toString() + System.getProperty("file.separator");
                if (FileSystemStore.isExistingSession(dir)) {
                    loaded = ws.loadSession(dir);
                } else {
                    loaded = ws.createSession(dir);
                }
            }
        }
        if (!loaded) {
            JOptionPane.showMessageDialog(null, "Conversations and other data will not be saved unless you create or open a session", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
    
